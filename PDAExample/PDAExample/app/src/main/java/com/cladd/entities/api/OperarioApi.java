package com.cladd.entities.api;

public class OperarioApi {
    private String logeo;
    private String descripcion;
    private String habilitaUsoTracker;
    private String planta;

    public String getLogeo() {
        return logeo;
    }

    public void setLogeo(String logeo) {
        this.logeo = logeo;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getHabilitaUsoTracker() {
        return habilitaUsoTracker;
    }

    public void setHabilitaUsoTracker(String habilitaUsoTracker) {
        this.habilitaUsoTracker = habilitaUsoTracker;
    }

    public String getPlanta() {
        return planta;
    }

    public void setPlanta(String planta) {
        this.planta = planta;
    }
}
